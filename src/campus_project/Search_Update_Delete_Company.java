    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/campus","root","");
            java.sql.Statement st = con.createStatement();
            String q,companyname,Seats_offered,Min_perc ,Max_Dip,offered;
            
            
            companyname = Comname.getText();
            q = "select * from company where cname= '" +companyname+ "'";
         
            ResultSet rs =st.executeQuery(q);
            if(rs.first())
            {
                Seats_offered = rs.getString("Seats_offered");
                Min_perc = rs.getString("Min_perc");
                Max_Dip = rs.getString("Max_Dip");
                offered = rs.getString("offered");

                txtseats.setText(Seats_offered);
                txtPer.setText(Min_perc);
                txtdip.setText(Max_Dip);
                txtpak.setText(offered);
                
            }
            else
            {
                JOptionPane.showMessageDialog(this , "No Company Record Found with Given Name...please check and then try again");
            }
            }
            catch(HeadlessException | ClassNotFoundException | SQLException e)
        {
        }
     
        
    }//GEN-LAST:event_jButton4ActionPerformed

