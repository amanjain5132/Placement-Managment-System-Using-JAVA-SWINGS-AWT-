    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        try {
            String cname = Comname.getText();
            String Seats_offered = txtseats.getText();
            String Min_perc = txtPer.getText();
            String Max_Dip = txtdip.getText();
            String offered = txtpak.getText();
                    

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/campus","root","");
            PreparedStatement ps = con.prepareStatement("Update company set Seats_offered=?,Min_perc=?,Max_Dip=?,offered=? where cname =?");
            
            
            ps.setString(1,Seats_offered);
            ps.setString(2,Min_perc);
            ps.setString(3,Max_Dip);
            ps.setString(4,offered);
            
            ps.setString(5,cname);
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Update Successful");
        } catch (HeadlessException | ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Search_Update_Delete_Company.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

